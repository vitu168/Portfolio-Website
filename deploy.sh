# Quick Deployment Commands

# Run these commands in PowerShell/Terminal to deploy your website:

# 1. Navigate to your project (if not already there)
cd "d:\Portfolio Website"

# 2. Check current status
git status

# 3. Add all your latest changes
git add .

# 4. Commit with deployment message
git commit -m "Deploy portfolio website with all features"

# 5. Push to GitHub
git push origin main

# After running these commands:
# 1. Go to https://github.com/vitu168/Portfolio-Website
# 2. Click "Settings" tab
# 3. Scroll to "Pages" section
# 4. Under "Source" select "Deploy from a branch"
# 5. Select "main" branch and "/" (root) folder
# 6. Click "Save"
# 7. Your website will be live at: https://vitu168.github.io/Portfolio-Website/

echo "Ready to deploy! Run the commands above one by one."
